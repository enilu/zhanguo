# 第十六回 东方尚借合从自壮 西土但凭法度观时

东方与秦之间的根本差异，已经不必靠大战便能看得很清。

东方之所以还能把众国暂时拢在一处，靠的是"合从"这面旗。

诸侯借它自壮，士人借它谈希望，近秦之国借它求不至独受其祸，远秦之国也借它抬一抬自己在大局里的位置。哪怕人人都知道这旗下面并非铁板一块，可只要旗还在，众国便还有一层可以共同出声、共同自安的名义。

这名义是有用的。

礼可因它整，文书可因它通，诸侯之间那点本来各自散乱的惧秦之心，也可因它暂时汇成同一股声气。若无这面旗，六国便更快退回各自求活、彼此观望的旧路。

可这一回也要补上一笔更冷的真相：名能壮心，未必壮骨。

因为旗一立，兵不会因此立刻更整，粮不会因此忽然更足，彼此多年不信也不会因此就真化为信。强国仍旧强在它本有之兵势，小国也仍旧弱在它本有之根底。合从之名可以让东方一时不至散得太快，却不能自动把六国底下那些本来不齐的骨头一夕接好。

苏秦对此越发明白。

他有时站在盟中高处看下去，能清楚感觉到：如今东方多的是"借名抬势"的人。赵借其名稳北方，燕借其名求不在局外，魏韩借其名不至立成弃子，齐楚也借其名，不至让自己在这场大势里显得太后知后觉。

人人都在借。

正因人人都借，这旗便一时更高。

可苏秦也知道，借来的势，若底下不够实，终究比不过另一种更稳的东西。

那便是秦。

秦在这一回里，仍未大举东动，也未见咸阳因东方合从先起什么额外喧声。可若把镜头转回函谷关内，便能看见另一种与东方全然不同的稳法。

---

咸阳宫中，秦王嬴驷正在阅一份来自东方的密报。

报上说，六国合从已成，苏秦佩六国相印，东方诸侯往来频密，似有共制西秦之意。

秦王看完，神色未变，只把密报放在案上，淡淡问道："诸卿以为，当如何应对？"

殿中群臣，有人主张即刻整军东向，以威压破其盟；有人建议遣使离间，先让六国彼此生疑；也有人认为，合从虽成，却未必能久，不如静观其变，待其自裂。

秦王听着，不置可否。

待众人说完，他才缓缓开口："合从之盟，寡人知之。苏秦之才，寡人亦知之。可寡人更知一事：六国之所以合，乃因惧秦；惧秦之心若一日不减，其盟便一日不散。可惧秦之心，亦六国各自之惧，非彼此之信。"

他顿了顿，目光扫过殿中诸臣："以惧合者，必以安离。今日六国惧秦而合，他日若秦不逼之太急，六国自会各算其安。到那时，盟虽在，心已散。"

"所以，"秦王的声音沉稳而冷峻，"寡人不急。急者，苏秦也。因为他知道，这盟是靠惧撑着的，惧若轻了，盟便重了。他要比寡人更急，更怕，更须日日修补。"

殿中一时无声。

秦王又道："传令函谷关，守备如常，不可轻动。再遣细作，入六国，观其动静，记其言行。不必急着离间，先看清他们各自想要什么、怕什么。待看清了，再动不迟。"

群臣领命而去。

秦王独坐殿中，望着东方，嘴角浮起一丝意味深长的笑意。

合从之旗，东方借之以自壮。

可他秦国的法度，却不需要借任何名义。法度就是法度，军功就是军功，耕战就是耕战。这些东西，不借名而自实，不倚势而自稳。

这才是真正的差距。

苏秦在东方，日日忙着补缝。

他在西方，只需等着那些缝自己裂开。
