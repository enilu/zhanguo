旧隙并没有因一面合从之旗而消失，只是暂时被旗面盖住了。

于是这一回里的苏秦，已不再奢望尽去众疑。

他只是更小心地把每一步都走得再实一些，让那些藏在客气底下的裂缝，不至于在他还没看见的时候就忽然裂开。

夜深时，他独对烛火，在竹简上又写下一行字：

"疑不可尽去，只可暂压。压之愈久，发之愈烈。"

他知道，这局棋还长着呢。
