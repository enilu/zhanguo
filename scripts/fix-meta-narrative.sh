# 第十五回 一身系印权重诸夏 四顾藏针势压群疑

同盟的裂纹既已一回回被看清，苏秦这"一身系印"的分量，反倒显得更重。

诸侯与群臣几乎都已习惯了一件事：凡盟中有疑，先问苏秦之意；凡几国轻重难分，先看苏秦如何排；凡有一处线头松动，也总是先想把消息送到苏秦案前，再看他会如何补这一缝。

这便是权重。

它不全来自六印，更来自前十几回里一回回压出来的事实：旁人能议局，真正能把局暂时维持住的人，眼下只有苏秦。

可权重越显，疑心也越跟着长。

赵国有人暗道："此人持印日久，诸国往来皆绕其手，权是否太重了些？"齐楚那边也有人私语："今日是借其持局，明日若诸侯事事都先问苏秦而后问君上，岂不又成另一层挟势？"便连平素最倚赖盟中声势的小国里，也偶有疑念：苏秦居中久了，会不会终究更看重大国之意，而把弱国之急放在后头？

这些疑，不是翻脸，却都像细针一样，慢慢藏进四顾目光之中。

苏秦对此并非不知。

他最清楚，自己今日之位，既令众国都需他，也令众国都不敢完全放心他。因为他越能持局，越不像只是某国一时门客；越不像一国门客，诸侯便越会防他是否借持局之名，坐大其权。

---

一日，盟中又起一场小危。

魏韩那边新得边报，说秦境兵马调动略有异样，于是急催赵国先明示援应之意；赵廷内部却有人觉得，边报未明，不宜轻易把自己先推到最前。魏使闻言，立时冷道："合从若到此时还只顾自保，魏韩今日何必还守其盟？"赵人也硬声道："赵倡其议，不等于赵要替诸国先把一切险都担尽。"

若换在前几轮，这类争执还可借"大义"先压一压；到如今，众人彼此都已太熟，旧话再说，只会更显空。

苏秦于是只做两件事。

先稳其势。

他分别给魏韩和赵廷各去一信，不提谁对谁错，只说"边报既至，合从之盟正当一用。先用其盟，再论其他"。这话给双方都留了台阶，又把焦点从"谁该先动"暂时移到了"盟如何用"上。

再定其序。

他提议，凡有边警，近秦诸国先以烽火为号，一日之内传至盟中；盟中诸国接报后，三日内须明示应之之意。有明序，便少了互相试探的空档；有定时，便少了拖延推诿的借口。

这个提议，各国虽不尽满意，却也没有更好的替代。于是勉强通过。

苏秦知道，这只是又补了一针，不是治本。可在这盘棋上，能一针一针地补下去，不让它忽然崩散，已经是他能做的极限了。

夜深时，他看着案上那枚六国相印，忽然想起自己当年在洛阳闭门苦读的日子。

那时他只想让天下看见自己。

如今天下都看见他了，他却常常觉得，自己比那时更孤单。

因为那时他只需对自己负责，如今他需对六国负责；那时他只需说服自己，如今他要说服的是六个各怀心思的诸侯。

印还在，人还在，可路却越来越难走了。
