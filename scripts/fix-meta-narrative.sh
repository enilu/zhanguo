# 第十四回 名盟初立人心未一 共势虽成旧隙犹深

盟书在案，并不等于万事就顺。

条文既定之后，一到真正往下执行，先前藏在礼数与雄谈底下的那些难处，反而一件件露得更明。

譬如守约。

纸上写着"有警共议"，可共议到哪一步才算守约？近秦之国一有边报，是立刻遣使四方，还是先由邻近数国会商？又譬如先后。谁先表明共守之意，谁又先担与秦对峙的实险？再譬如分责。强国若兵多地厚，是否便自然该多担；弱国若国小势窄，是否便永远可以以"自保不易"为由少尽其责？

这些问题，前几回也提过，可到了这一回，已经不再只是议席上的口舌，而是制度一落纸、执行一落地就立刻变薄的地方。

有的国在文书里附和得极快，真正要遣使、备粮、定次序时却忽然拖延；有的国嘴上说不争名，私下却总要在礼仪和称序里添减一笔，不肯真把自己摆到与别国齐平的位置上。还有些国家，最善于"表面尊盟，手上留缝"：明着不逆，暗里却总给自己多留一条退路。

同盟并非虚假，只是人人都在按自己利益去重写它。

这便是这一回真正要清账的地方。

---

一日，苏秦连着看了三份不同国家送来的文书。赵国来简，说近秦应变之议，不能总由赵先出例；魏使附文，则再问若秦试边时，强国援应是否应有更明白承诺；齐国那边措辞最圆，只说"盟既成，当求众国各安其位"，看似平和，实则仍在强调大国不愿轻受拘束。

