# 第十二回 强国争衡难忘旧算 小邦附义各有私谋

邯郸城中，春深时节，馆驿间的走动比往日更密了。

苏秦坐在案前，看着各国送来的文书，眉头微蹙。盟议虽成，可真正往下运转时，他才发现强国与小国对"合从"这两个字，原本就不是同一种理解。

赵、齐、楚这类有分量的国家，怕的从来不只是秦，也怕合从之后自己的主导权被悄悄稀释。它们愿借同盟分秦压，却不愿平白把自己多年积累的山河兵势，变成人人都可拿来要求它先尽责的理由。故强国口里也说共制西秦，心里想的却常是：此盟既要成，谁来居中？谁来定先后？谁的声音到最后最算数？

而魏、韩、燕这类更偏弱、更偏近祸，或更需借势自保的国家，所想又全然不同。

它们当然盼同盟是真，因为不同盟，自己往往更快先受其祸；可也正因如此，它们最怕的便是"同盟一成，自己反倒被推到最前"。口里共义，若最后落到本国头上的只是风险与牺牲，而援手总在后面，那这盟于它们便不是护身之网，而是另一层更好听的束缚。

所以这一回里，表面上六国都坐在合从之名下，心里却各自另有账本。

赵国私议时，有人道："赵先倡其议，本为不独战，不可因已居其前，便事事都由赵先担。"齐臣则冷看北方局势，心道："若众国都需齐来为其增重，齐自应保其发言之位，不可只出其力而不主其局。"楚国来使说话更圆，却处处带着"楚国岂可轻受约束"的意思。

另一边，魏韩私谈时却常常只绕着一句话打转：若秦先逼，谁会真来？燕国心思也细，它固然想借合从抬身，却也怕一旦强国重新排位，燕又被自然挤回局边。

这等差异，不是哪一句大义能抹平的，而是国家位置、国力轻重、离秦远近、本来旧账决定的。

苏秦在这一回，第一次真正感觉到，自己面对的已不只是"大家意见不同"，而是大家站的位置不同，所以从根上便看同盟不是一回事。

一日，他与几国代表同坐，席间并无激烈争吵，人人说话都极客气。可细听之下，句句都在为自己留余地。

齐使道："齐自不避共制西秦之义，只是凡议定诸事，总该有个不失大国体面的安排。"韩使便应："小国体面原也不值什么，只是小国若总先担险，长此以往，谁还敢真信同盟之名？"赵使又说："赵不辞其责，但不可把赵之能战，看成理所当然多担。"燕使最后才轻轻添了一句："燕愿附义，可也不愿盟中只有强国彼此相议，弱国只在外头等传话。"

四人说得都很平，平得像谁也没在争。可苏秦却听得出，这比上一回大声争执更难。

因为大声争，还能看见矛头；这种客气里的加减，才是真正持久的裂缝。

---

苏秦回到馆舍，独坐至夜深。

窗外月色清冷，照在案上那几份文书上。他忽然想起自己初出洛阳时，只道天下之势，说清楚了便可动人；如今才知，势这东西，从来不是单靠说便能定死的。

强国要的是名实相符的体面，小国要的是不被牺牲的保全，远国要的是不被动摇的观望余地，近国要的是有人先替它挡一挡的实在承诺。这些要求，单看哪一个都不算过分，可放在一起，便成了一盘永远下不完的棋。

他提起笔，在竹简上写下几个字：

"盟可立，心难一。"

写完之后，又添了一句：

"非盟之罪，乃人之常情。"

这夜风从窗缝间透进来，带着北方草原的气息。苏秦望着那一线夜色，知道往后的路，只会比先前更难走。

因为合从之旗虽已竖起，可旗下面的人，各自都还在按自己的旧账活着。
