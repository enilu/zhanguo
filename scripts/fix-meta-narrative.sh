# 第十七回 众国相安只凭时势 一人持局未得长宁

这一回里，合从局面看上去反倒比前几回更平。

文书照例往来，诸侯表面守约，馆驿间虽仍有轻重试探，却少了先前那些当场几欲顶死的激烈冲撞。边报未有大变，秦也未骤然东压，故众国都还肯维持这一层"同在一旗之下"的秩序。

若只看表面，似乎这一局已经渐渐稳住。

可真正看得深的人都明白，这份相安主要不是因为大家终于信了彼此，而是因为秦还在外面，外压仍旧重着。共同之患还没有轻到可以放手各算，所以那些旧隙、旧疑、旧账，才暂时还被压在下面。

也就是说，压住不安的，是时势，不是信任。

魏韩私下仍会反复盘算：若秦下一步更逼，强国是否真会如盟书所言那般应之？齐楚也仍旧在心里记着，自己究竟该把这盟当作可久持之局，还是当作眼前一段不得不借的名势。赵虽居其中，亦未曾真正忘记"自己不可被天下借得太过"。燕国则时不时仍要提醒众人，北方之势里不能总把燕看作附属陪衬。

这些都在，只是暂时未被外力拨到最响处。

苏秦仍在持局。

可到了这一回，他做事的样子，已经越来越像一个不断修补边角的人。今日才把一份言辞偏重的赵国文书改平几分，免得远国生疑；明日又要劝魏韩不必因一点边上风声就立时疑盟；后日还得安抚燕使，让它知道自己并未在北方线里被自然挤开。

这些事都不大，却没有一件是可不做的。

正因局面表面还稳，修补便更要细。真等裂口大到人人看见时，再去补，多半就晚了。

---

一日傍晚，苏秦与数国使者共饮，席间大家都守着分寸，言笑也算平和。席散之后，赵国那位年轻议者送他出门，低声道："这几月看来，诸国总算还能相安。"

苏秦望着馆外沉沉暮色，道："相安是真相安，却未必是长安。"

那人一怔："何意？"

苏秦回头看他，目光里带着几分疲惫，却也带着几分清醒："今日之相安，因秦在故。若秦一日不逼，诸国便一日还肯同在一旗之下。可秦若忽然动了，或忽然不动了，这相安便立刻要试出真假。"

"秦不动，不是更好？"那人不解。

"秦不动，"苏秦苦笑，"诸国便有更多工夫去算自己的账。算得久了，合从之义便薄了。秦若动，诸国还知要合；秦若久不动，诸国便忘了为何要合。"

那人默然。

苏秦又道："所以我比谁都怕秦不动，也比谁都怕秦大动。一动一静之间，这局棋随时可能翻盘。"

他说完，转身离去，背影在暮色中显得有些孤单。

那人站在原地，望着他的背影，忽然觉得，这位佩六国相印的纵横之士，其实比任何人都更清楚：自己手中这局棋，从来就没有真正稳过。

