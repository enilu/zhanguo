回到馆舍，苏秦独坐至夜深。

烛火摇曳，把他的影子投在墙上，拉得很长很长。

他想起自己这些日子以来，日日周旋于六国之间，说尽了千言万语，费尽了千般心思。可到头来，他所能做的，不过是让这局棋暂时不散，而不是让这局棋真正成局。

因为真正的局，不是他说了算的。

真正的局，在秦那边。

秦动，东方才能合；秦静，东方便要散。这是苏秦最清楚不过的道理，却也是最让他无力的道理。

他提起笔，在竹简上写下几个字：

"持局者，非局之主。"

写完之后，他望着那行字，久久未动。

窗外，夜色深沉，万籁俱寂。

可他知道，在这寂静之下，藏着多少暗流涌动。

秦在等。

六国在算。

而他，只能继续补下去。
